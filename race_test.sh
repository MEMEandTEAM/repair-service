URL="http://localhost:8000/api/requests/1/take"

echo "Проверка защиты от состояния Race Condition"
echo "Отправляем два параллельных POST-запроса от двух мастеров на взятие заявки #1..."
echo "Ожидаемый результат: один запрос вернет 'Заявка успешно взята в работу', второй - '409 Conflict'."
echo "---------------------------------------------------"

curl -s -X POST $URL &
PID1=$!

curl -s -X POST $URL &
PID2=$!

wait $PID1
wait $PID2

echo -e "\n---------------------------------------------------"
echo "Тест завершен."